export function posterFieldLabel(key, lang) {
  const code = lang === "pt" ? "pt" : "en";
  const map = code === "pt" ? FIELD_LABELS_PT : FIELD_LABELS_EN;
  return map[key] || map[key] || key.replace(/_/g, " ").replace(/\b\w/g, (c) => c.toUpperCase());
}

