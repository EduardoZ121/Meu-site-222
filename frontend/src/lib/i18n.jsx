// Replace {key} occurrences with provided values.
function format(str, vars) {
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : `{${k}}`));
}

const I18nCtx = createContext(null);

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem("rp_lang") || "pt");
  const switchLang = (l) => { setLang(l); localStorage.setItem("rp_lang", l); };
  const t = (key, vars) => format(dict[lang]?.[key] || dict.pt[key] || key, vars);
  return <I18nCtx.Provider value={{ lang, t, switchLang, langs: Object.keys(dict) }}>{children}</I18nCtx.Provider>;
}

export const useI18n = () => useContext(I18nCtx);
